package models;

public class Adder {

    public Integer adding(Integer number1, Integer number2){
        return number1 + number2;
    }

    public Integer adding(Integer number1, Integer number2, Integer number3){
        return number1 + number2 + number3;
    }

    public Integer adding(Integer number1, Integer number2, Integer number3, Integer number4){
        return number1 + number2 + number3 + number4;
    }
}
